#include "libft.h"

void	*ft_memset(void *a, int b, size_t n)
{
	char	*s;
	size_t	i;

	i = 0;
	s = (char*)a;
	while (i < n)
	{
		s[i] = b;
		i++;
	}
	return (a);
}
